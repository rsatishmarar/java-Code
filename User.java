package com.vks.plugIn.bean;

public class User {

	private String userType;
	private String userName;
	private String password;
	private String userAdd1;
	private String userAdd2;
	private String userCity;
	private String userPinCD;
	private String userPhoneNum;
	private String userEmailId;

	public String getUserType() {
		return userType;
	}

	public void setUserType(String userType) {
		this.userType = userType;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getUserAdd1() {
		return userAdd1;
	}

	public void setUserAdd1(String userAdd1) {
		this.userAdd1 = userAdd1;
	}

	public String getUserAdd2() {
		return userAdd2;
	}

	public void setUserAdd2(String userAdd2) {
		this.userAdd2 = userAdd2;
	}

	public String getUserCity() {
		return userCity;
	}

	public void setUserCity(String userCity) {
		this.userCity = userCity;
	}

	public String getUserPinCD() {
		return userPinCD;
	}

	public void setUserPinCD(String userPinCD) {
		this.userPinCD = userPinCD;
	}

	public String getUserPhoneNum() {
		return userPhoneNum;
	}

	public void setUserPhoneNum(String userPhoneNum) {
		this.userPhoneNum = userPhoneNum;
	}

	public String getUserEmailId() {
		return userEmailId;
	}

	public void setUserEmailId(String userEmailId) {
		this.userEmailId = userEmailId;
	}

}
